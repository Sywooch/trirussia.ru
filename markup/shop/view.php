								<h2>Трасса</h2>
								<p>Многие знают трассу вокруг озера Бельского лучше, чем дорожки родного парка рядом с домом. Все знают, где ждать подвоха (около футбольных полей), где можно отпустить себя и ускориться на спуске (рядом с храмом), где потолкаться (перед последним перед финишем разворотом) и где начинать финишировать (сразу после выхода из узкой зоны). Но в этом году отдельно стоит отметить качество покрытия: идеальный ровный асфальт без снега и льда. У меня во дворе на третий день после снегопада не так чисто. Большие молодцы!</p>

								<h2>Болельщики</h2>
								<p>С каждым годом зрителей всё больше и больше. Отдельно хочется отметить, что некоторые приезжают целыми дружными семьями: с детьми, бабушками и дедушками. Раздавать «хай файвы» довольным детишкам на финише — одно удовольствие! В следующем году Алексей Ческидов обещал, что старт будет проходить через центр города, поэтому количество болельщиков должно кратно увеличиться.</p>
								<h2>Спасибо</h2>
								<p>Хочу сказать спасибо всем, кто принимал участие в организации гонки, всем волонтёрам (вы вообще самые крутые), сотрудникам ДПС (я каждый год рад видеть таких приятных и участливых сотрудников), Алексею Ческидову, Ольге Шавыриной, Давиду Шахзадяну и 1000 человек, которые разделили со мной то отличное воскресенье!</p>
								<h2>Выводы</h2>
								<p>Февральский марафон «Титан» становится отличной традицией. В следующем году нужно что-то придумывать: всем желающим просто места не хватит!</p>

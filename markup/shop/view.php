								<h2>Парковка</h2>
								<p>Похоже, озеро Бельское и школьный спортзал выросли из забегов серии «Титан». Впервые были серьёзные проблемы с парковкой, но спасибо ребятам, которые быстро и эффективно расставляли приезжающие автомобили. В итоге «очередь» на парковку заняла не более 10 минут. Машин было очень много. В Москве на любом бы старте с таким количеством автомобилей точно случился бы коллапс.</p>
								<h2>Стартовый пакет</h2>
								<p>Во-первых, нужно отметить, что идея с предварительным получением стартовых пакетов работает отлично. Я побывал в магазине Триспорт на Курской, буквально за минуту получил номер и купил смазку для цепи. Это отличный симбиоз организаторов и спортивной розницы. Во-вторых, призываю всех получать стартовые пакеты заранее: это сэкономит вам время и нервы. На месте старта очередь за номерами была минут на 15—20.</p>
								<img src="https://process.filestackapi.com/AMUpLpMHnQfWBIrqRcYd3z/resize=width:1200,height:750,fit:crop/http://www.fun-run.ru/wp-content/uploads/2016/02/marathon-titan-2016-3.jpg" class="img-fluid m-t-1 m-b-1">
								<p class="small m-t-0"><a href="http://www.fun-run.ru/2016/02/07/marafon-titan-2016/" class="underline">Фотография Марии Шальневой</a></p>
								<p>Немного расстроило, что в конверте было мало «подарков». Безусловно, это несущественная мелочь и современный тренд (сложно положить в пакет столько же, сколько и раньше, при дорогом долларе и таком же стартовом взносе), но раньше в пакетах были банданы, баффы, батончики, купоны, гели и ещё куча всего. А сейчас только номер, пара флаеров и булавки. Думаю, что это новый стандарт и к нему нужно привыкать.</p>
								<h2>Камера хранения</h2>
								<p>Как я писал выше, школьный спортзал стал слишком тесен для тысячи спортсменов и болельщиков. Поэтому ценный совет на будущее: переоденьтесь в машине. От парковки до тёплого здания метров 200, поэтому бегом замёрзнуть точно не успеете. Ни до гонки, ни после. В камеру хранения самая длинная и самая весёлая очередь (все в предвкушении первого большого старта сезона) —&nbsp;20 минут.</p>
								<h2>Экспо</h2>
								<p>Количество продавцов приятно удивило. Во-первых, отличный стенд самого Титана. Брендированный велосипед, форма, шапочки, фляги и ещё куча всего триатлонного. Во-вторых, уже постоянные участники крупных спортивных событий —&nbsp;ребята с гелями <a href="http://www.trirussia.ru/magazine/read/?i=power-up" class="underline" target="_blank">PowerUp</a>. Можно было купить практически всё, что угодно. Кроме одного: ни у кого не было шапок или баффов. Я легкомысленно не взял с собой шапку, поэтому использовал в качестве неё бафф. А вот горло осталось без прикрытия.</p>

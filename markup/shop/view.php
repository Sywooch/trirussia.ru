								<h2>Обо всём по порядку</h2>
								<p>Традиционно регистрация открылась за несколько месяцев до старта. Но это не помешало многим отложить всё на последний момент и в итоге оказаться без слота. В этом году было 1000 слотов, все из которых были выкуплены. И это в феврале и в 40 километрах от Москвы. Когда я впервые бежал 10 км в Бронницах года 3 назад, то даже и представить себе не мог, что когда-нибудь здесь будет так много людей! Но обо всём по порядку.</p>
